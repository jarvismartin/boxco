    <h3 class="my-2 font-semibold">The website should:</h3>
    <p>
      - be hosted and publicly accessible have a styling library/technology
      implemented
    </p>
    <p class="m-8 italic">
      I chose Tailwind CSS (with PostCSS) because it’s popular (well-supported
      and easy to find devs who know it or are excited to learn and use it, I
      think???), powerful, well-maintained, smaller than other options
      (performance). Speed and consistency. However, we could just as easily use
      Carbon, Bootstrap, etc. – whatever works best (read “most comfortable”)
      for the people paying for it. We could even use (some or all of) the
      customer’s css (e.g. scraped from their site) to do anything from white
      label the app to use a color palette consistent with their brand tooling,
      etc.
    </p>
