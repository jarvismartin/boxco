    <p>
      - be able to update order with status, e.g. processing, canceled,
      delivered.
    </p>
    <h3 class="mb-2 mt-8 font-semibold">We care about:</h3>
    <p>
      - the scalability of the solution. Think of it as if there were 100, 10
